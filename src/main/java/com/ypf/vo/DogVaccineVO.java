package com.ypf.vo;

public class DogVaccineVO {

	private Integer id;

    private String dogName;
    
    private String dogKind;
    
    private String dogSex;
    
    private String dogAge;
    
    private String dogVaccineName;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getDogName() {
		return dogName;
	}

	public void setDogName(String dogName) {
		this.dogName = dogName;
	}

	public String getDogKind() {
		return dogKind;
	}

	public void setDogKind(String dogKind) {
		this.dogKind = dogKind;
	}

	public String getDogSex() {
		return dogSex;
	}

	public void setDogSex(String dogSex) {
		this.dogSex = dogSex;
	}

	public String getDogAge() {
		return dogAge;
	}

	public void setDogAge(String dogAge) {
		this.dogAge = dogAge;
	}

	public String getDogVaccineName() {
		return dogVaccineName;
	}

	public void setDogVaccineName(String dogVaccineName) {
		this.dogVaccineName = dogVaccineName;
	}

    
}
